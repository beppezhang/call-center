package bz.sunlight.dto;

public class CustomerTagDTO {

	private String id;
	private String customerTag;
	private String name;
	private String mobile;
	private Short sex;
	private Short isRepeat;
	private Short dialTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCustomerTag() {
		return customerTag;
	}
	public void setCustomerTag(String customerTag) {
		this.customerTag = customerTag;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public Short getSex() {
		return sex;
	}
	public void setSex(Short sex) {
		this.sex = sex;
	}
	public Short getIsRepeat() {
		return isRepeat;
	}
	public void setIsRepeat(Short isRepeat) {
		this.isRepeat = isRepeat;
	}
	public Short getDialTime() {
		return dialTime;
	}
	public void setDialTime(Short dialTime) {
		this.dialTime = dialTime;
	}
	
	
	
	
	
}
